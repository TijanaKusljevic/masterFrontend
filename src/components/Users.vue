    watch: {
      dialog (val) {
        val || this.close()
      },
      dialogDelete (val) {
        val || this.closeDelete()
      },
    },

    mounted () {
      this.initialize()
    },

    methods: {
      initialize () {

        console.log("init");

        axios.get("http://localhost:47000/api/allUsers")
